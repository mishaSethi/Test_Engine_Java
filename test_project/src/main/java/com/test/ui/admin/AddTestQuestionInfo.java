    private void btnaddquestioninfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddquestioninfoActionPerformed
        boolean allvalid = true;
        String message = "";
        String question = jtfquestiontext.getText().trim();
        String optiona = jtfoptiona.getText().trim();
        String optionb = jtfoptionb.getText().trim();
        String optionc = jtfoptionc.getText().trim();
        String optiond = jtfoptiond.getText().trim();
        String marks = jtfmarks.getText().trim();
        String answer = (String) jcbanswer.getSelectedItem();
        if (Validations.isEmpty(question)) {
            message = "Please Enter The text In Question Box";
            allvalid = false;
        }
        if (Validations.isEmpty(optiona)) {
            message = "Please Enter Option A";
            allvalid = false;
        }
        if (Validations.isEmpty(optionb)) {
            message = "Please Enter Option B";
            allvalid = false;
        }
        if (Validations.isEmpty(optionc)) {
            message = "Please Enter Option C";
            allvalid = false;
        }
        if (Validations.isEmpty(optiond)) {
            message = "Please Enter Option D";
            allvalid = false;
        }
        if (jcbanswer.getSelectedIndex() == 0) {
            message = "Please Choose Answer";
            allvalid = false;
        }
        if (jcbtestid.getSelectedIndex() == 0) {
            message = "Please Choose Test ID";
            allvalid = false;
        }
        if (jcbsectionid.getSelectedIndex() == 0) {
            message = "Please Choose Section";
            allvalid = false;
        }
        if (Validations.isEmpty(marks)) {
            message = "Please Enter Marks";
            allvalid = false;
        }
        if (allvalid) {
            TestQuestionTO tqi = new TestQuestionTO();
            tqi.setQuestiontext(question);
            tqi.setOptiona(optiona);
            tqi.setOptionb(optionb);
            tqi.setOptionc(optionc);
            tqi.setOptiond(optiond);
            int marks1 = Integer.parseInt(marks);
            tqi.setMarks(marks1);
            TestInfoTO tit = (TestInfoTO) jcbtestid.getSelectedItem();
            tqi.setTestid(tit.getTestid());
            SectionInfoTO sit = (SectionInfoTO) jcbsectionid.getSelectedItem();
            tqi.setSectionid(sit.getSectionid());

            tqi.setAnswer(answer);
            TestQuestionDAO tqd = new TestQuestionDAO();
            if (tqd.insertRecord(tqi)) {
                message = "Question Is Added In The System";
            } else {
                message = tqd.getErrormessage();
            }
            JOptionPane.showMessageDialog(this, message);
        }

    }//GEN-LAST:event_btnaddquestioninfoActionPerformed

