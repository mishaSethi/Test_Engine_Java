    private void jcbtestidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbtestidItemStateChanged
        jcbsectionid.removeAllItems();
        jcbsectionid.addItem("Sections");
        if (jcbtestid.getSelectedIndex() > 0) {

            TestInfoTO tit = (TestInfoTO) jcbtestid.getSelectedItem();
            List<SectionInfoTO> sections = new SectionInfoDAO().getAllRecord(Integer.toString(tit.getTestid()));
            if (sections != null && sections.size() > 0) {
                for (SectionInfoTO sit : sections) {
                    jcbsectionid.addItem(sit);
                }
            }
        }

    }//GEN-LAST:event_jcbtestidItemStateChanged


